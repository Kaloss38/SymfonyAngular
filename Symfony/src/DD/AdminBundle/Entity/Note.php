<?php

namespace DD\AdminBundle\Entity;

/**
 * Note
 */
class Note
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var string
     */
    private $autor;

    /**
     * @var string
     */
    private $content;

    /**
     * @var \DateTime
     */
    private $date;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set autor
     *
     * @param string $autor
     *
     * @return Note
     */
    public function setAutor($autor)
    {
        $this->autor = $autor;

        return $this;
    }

    /**
     * Get autor
     *
     * @return string
     */
    public function getAutor()
    {
        return $this->autor;
    }

    /**
     * Set content
     *
     * @param string $content
     *
     * @return Note
     */
    public function setContent($content)
    {
        $this->content = $content;

        return $this;
    }

    /**
     * Get content
     *
     * @return string
     */
    public function getContent()
    {
        return $this->content;
    }

    /**
     * Set date
     *
     * @param \DateTime $date
     *
     * @return Note
     */
    public function setDate($date)
    {
        $this->date = $date;

        return $this;
    }

    /**
     * Get date
     *
     * @return \DateTime
     */
    public function getDate()
    {
        return $this->date;
    }
    /**
     * @var \DD\AdminBundle\Entity\Project
     */
    private $project;


    /**
     * Set project
     *
     * @param \DD\AdminBundle\Entity\Project $project
     *
     * @return Note
     */
    public function setProject(\DD\AdminBundle\Entity\Project $project = null)
    {
        $this->project = $project;

        return $this;
    }

    /**
     * Get project
     *
     * @return \DD\AdminBundle\Entity\Project
     */
    public function getProject()
    {
        return $this->project;
    }
    /**
     * @var \DD\AdminBundle\Entity\User
     */
    private $user;


    /**
     * Set user
     *
     * @param \DD\AdminBundle\Entity\User $user
     *
     * @return Note
     */
    public function setUser(\DD\AdminBundle\Entity\User $user = null)
    {
        $this->user = $user;

        return $this;
    }

    /**
     * Get user
     *
     * @return \DD\AdminBundle\Entity\User
     */
    public function getUser()
    {
        return $this->user;
    }
}
